    private void add(String a, Integer b, Integer c) {

        if (tabla.getValueAt(n, n) != null) {
            n++;
        }

        tabla.setValueAt(a, n, 0);

        tabla.setValueAt(b, n, 1);

        tabla.setValueAt(c, n, 2);

    }

    private boolean isNumber(String a) {
        try {
            Integer b = Integer.parseInt(a);
        } catch (Exception e) {
            return false;
        }
        return true;
    }

