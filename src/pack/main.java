    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed

        if (concepto.getText() == null || cant.getValue() == null || precio.getText() == null) {

            JOptionPane.showMessageDialog(this, "Favor de llenar todos los formularios correctamente", "Error", JOptionPane.ERROR_MESSAGE);
        }

        String concept = concepto.getText();
        Integer can = (Integer) cant.getValue();
        String temp = precio.getText();

        if (isNumber(temp)) {
            Integer price = Integer.parseInt(temp);
            add(concept, can, price);

            float venta = can * price;
            tot = tot + venta;
            totall.setText("Venta=" + tot);

        } else {
            JOptionPane.showMessageDialog(this, "Favor de ingresar valores numericos solamente", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_addActionPerformed

